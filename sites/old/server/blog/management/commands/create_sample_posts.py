class Command(BaseCommand):
    help = 'Create 3 sample blog posts for SEO and content testing'

    def handle(self, *args, **kwargs):
        created = 0
        skipped = 0

        for data in POSTS:
            if Post.objects.filter(title=data['title']).exists():
                self.stdout.write(f'  skip (exists): {data["title"]}')
                skipped += 1
                continue

            post = Post.objects.create(
                title=data['title'],
                description=data['description'],
                rich_content=data['content'],
                is_published=True,
            )

            courses = Course.objects.filter(tag=data['tag'], is_active=True)
            if courses.exists():
                post.courses.set(courses)
                self.stdout.write(
                    f'  linked to {courses.count()} course(s): {data["title"]}'
                )

            created += 1
            self.stdout.write(self.style.SUCCESS(f'  created: {data["title"]}'))

        self.stdout.write(
            self.style.SUCCESS(f'\nDone — created: {created}, skipped: {skipped}')
        )
